        function createAxes(obj, parent)
            % 创建绘图坐标轴
            obj.controls.axes_handles = cell(2, 2);
            
            for i = 1:2
                for j = 1:2
                    idx = (i-1)*2 + j;
                    obj.controls.axes_handles{i,j} = subplot(2,2,idx, 'Parent', parent);
                end
            end
        end
        
        function designController(obj)
            % 设计MPC控制器
            if isempty(obj.controller.plant_model)
                errordlg('请先设置系统模型', '错误');
                return;
            end
            
            if ~exist('mpc', 'file')
                errordlg('需要MPC Toolbox', '错误');
                return;
            end
            
            try
                % 获取设计参数
                prediction_horizon = str2double(get(obj.controls.prediction_horizon, 'String'));
                control_horizon = str2double(get(obj.controls.control_horizon, 'String'));
                sample_time = str2double(get(obj.controls.sample_time, 'String'));
                output_weight = str2double(get(obj.controls.output_weight, 'String'));
                control_weight = str2double(get(obj.controls.control_weight, 'String'));
                
                if any(isnan([prediction_horizon, control_horizon, sample_time, output_weight, control_weight]))
                    errordlg('请输入有效的设计参数', '错误');
                    return;
                end
                
                % 设置设计参数
                design_params = struct();
                design_params.prediction_horizon = prediction_horizon;
                design_params.control_horizon = control_horizon;
                design_params.sample_time = sample_time;
                design_params.output_weight = output_weight;
                design_params.control_weight = control_weight;
                
                obj.controller.setDesignParams(design_params);
                
                % 设计控制器
                obj.controller.design();
                
                % 更新UI
                obj.updateUI();
                
                msgbox('MPC控制器设计完成', '成功');
                
            catch ME
                errordlg(['设计失败: ', ME.message], '错误');
            end
        end
        
        function updateUI(obj)
            % 调用父类的通用更新
            updateUI@ControllerUIBase(obj);
            
            % 子类特定的更新：MPC参数显示可以在这里添加
        end
    end
